//Program to print 5 numbers

#include <iostream>
using namespace std;

int main()
{
    int count;
    count = 0;

    while ( count < 5 )
    {
        cout << count << " ";

        count = count + 1;
    }

    cout << endl << count << endl;

    cout << "end of the prgram" << endl;

return 0;
}